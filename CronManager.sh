    echo -e "${amarillo} El script se ha actualizado, es necesario cargarlo de nuevo.${borra_colores}"
    echo -e ""
    read -p " Pulsa una tecla para continuar." pause
    exit
fi
}

software_necesario(){
#funcion software necesario
#para que funcione necesita:
#   conexion a internet
